Cubemap::Cubemap(std::vector<std::string>& faces)
{
	m_index_count = skybox_vertices.size();
	m_texture = TextureCubemap::Create(faces);

	m_VAO = VertexArray::Create();
	m_VAO->Bind();

	m_VBO = VertexBuffer::Create(skybox_vertices.data(), skybox_vertices.size() * sizeof(float));
	m_VBO->SetLayout(BufferLayout
		{
			BufferElement{ ShaderDataType::Float3, std::string("vin_vertices"), false },
		});

	m_VAO->AddVertexBuffer(m_VBO);
	m_VAO->Unbind();
}
